package wrapperclasses;

public class autoboxingprogram {

	public static void main(String[] args) {
		// auto-boxing program
		
				Integer i = 20;
		        int a = i;  

		        Double d = 33.44;
		        double val = d;

		        Boolean b = true;
		        boolean bool = b;

		        System.out.println("Auto-unboxed int: " + a);
		        System.out.println("Auto-unboxed double: " + val);
		        System.out.println("Auto-unboxed boolean: " + bool);
			}

		}
