package wrapperclasses;

public class unboxingprogram {

	public static void main(String[] args) {
		// unboxing program
		
				Integer i = new Integer(20);
		        int a = i.intValue();  

		        Double d = new Double(45.67);
		        double val = d.doubleValue();

		        System.out.println("Unboxed int: " + a);
		        System.out.println("Unboxed double: " + val);
			}

		}
